//operadores aritmeticos, logicos, relacionais e de atribuição
// Operadores Aritméticos
let a = 10;
let b = 5;

let sum = a + b; // Soma
let difference = a - b; // Subtração
let product = a * b; // Multiplicação
let quotient = a / b; // Divisão
let remainder = a % b; // Módulo (resto da divisão)
let exponentiation = a ** b; // Exponenciação
console.log("Soma:", sum);
console.log("Subtração:", +(a - b)); // Subtração com operador unário
console.log("Subtração:", difference);
console.log("Multiplicação:", product);
console.log("Divisão:", quotient);
console.log("Módulo:", remainder);
console.log("Exponenciação:", exponentiation);
