// operadores de atribuição são usados para atribuir valores a variáveis
// Operadores de Atribuição
let a = 10; // Atribuição simples
let b = 5; // Atribuição simples
let c = 2; // Atribuição simples

//atribuição simples
a = b; // Atribuição de b a a
//atribuição composta
a += b; // Adição e atribuição (a = a + b)
b -= c; // Subtração e atribuição (b = b - c)
c *= 2; // Multiplicação e atribuição (c = c * 2)
a /= 2; // Divisão e atribuição (a = a / 2)
x += 5; // Adição e atribuição (x = x + 5)
